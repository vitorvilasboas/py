"""
Exemplo 004: Leia um número inteiro e imprima sua metade, seu antecessor, seu sucessor, seu quadrado e seu cubo.
"""

num = int(input("Digite um número qualquer: "))

print("Metade: ", num/2)
print("Antecessor: ", num-1)
print("Sucessor: ", num+1)
print("Quadrado: ", num**2)
print("Cubo: ", num**3)

print("Metade: ", num/2, "\n\n\n\n\nAntecessor: ", num-1, "\nSucessor: ", num+1,
      "\nQuadrado: ", num**2, "\nCubo: ", num**3)